public class Bitwise {
    public static void main(String[]args)
    {
        int x=12,y=13;
        System.out.println("X&Y ="+(x&y));
        System.out.println("X|Y ="+(x|y));
        System.out.println("X^Y ="+(x^y));
        System.out.println("X<<2 ="+(x<<2));
        System.out.println("X>>2 ="+(x>>2));
        System.out.println("~X="+(~x));
    }
}
