public class Variables {
    public static void main(String[]args)
    {
        int a=125, b=12345;
        long ax=1234567890;
        short d=4043;
        float fl=2.13459f;
        double dx=1.1415927;
        char c='w';
        System.out.println("a+c ="+a+c);
        System.out.println("fl+c ="+fl+c);
        System.out.println("dx+fl ="+dx+fl);
        System.out.println("((int)dx)+ax ="+((int)dx)+ax);
        System.out.println("a+fl ="+a+fl);
        System.out.println("d+b ="+d+b);
        System.out.println("ax+b ="+ax+b);
        System.out.println("d+c ="+d+c);
        System.out.println("ax+c ="+ax+c);
    }
    
}
